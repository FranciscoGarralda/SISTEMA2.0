#!/usr/bin/env node

const fs = require('fs');
const path = require('path');

console.log('üîç VERIFICACI√ìN DE SALUD DEL SISTEMA 2.0');
console.log('==========================================\n');

// Verificar Node.js
console.log('‚úÖ Node.js:', process.version);

// Verificar NPM
const npmVersion = require('child_process').execSync('npm --version', { encoding: 'utf8' }).trim();
console.log('‚úÖ NPM:', npmVersion);

// Verificar archivos de configuraci√≥n
const configFiles = [
  '.env.local',
  'package.json',
  'netlify.toml',
  'next.config.js',
  'tailwind.config.js'
];

console.log('\nüìÅ ARCHIVOS DE CONFIGURACI√ìN:');
configFiles.forEach(file => {
  if (fs.existsSync(file)) {
    console.log(`‚úÖ ${file}`);
  } else {
    console.log(`‚ùå ${file} - FALTANTE`);
  }
});

// Verificar dependencias
console.log('\nüì¶ DEPENDENCIAS:');
const packageJson = JSON.parse(fs.readFileSync('package.json', 'utf8'));
const requiredDeps = ['next', 'react', 'react-dom', '@netlify/functions'];
const requiredDevDeps = ['concurrently', 'netlify-cli'];

requiredDeps.forEach(dep => {
  if (packageJson.dependencies[dep]) {
    console.log(`‚úÖ ${dep}: ${packageJson.dependencies[dep]}`);
  } else {
    console.log(`‚ùå ${dep} - FALTANTE`);
  }
});

requiredDevDeps.forEach(dep => {
  if (packageJson.devDependencies[dep]) {
    console.log(`‚úÖ ${dep}: ${packageJson.devDependencies[dep]}`);
  } else {
    console.log(`‚ùå ${dep} - FALTANTE`);
  }
});

// Verificar scripts
console.log('\nüöÄ SCRIPTS DISPONIBLES:');
const requiredScripts = ['dev:full', 'health', 'test:full', 'db:setup'];
requiredScripts.forEach(script => {
  if (packageJson.scripts[script]) {
    console.log(`‚úÖ ${script}`);
  } else {
    console.log(`‚ùå ${script} - FALTANTE`);
  }
});

// Verificar funciones Netlify
console.log('\n‚ö° FUNCIONES NETLIFY:');
const functionsDir = 'netlify/functions';
if (fs.existsSync(functionsDir)) {
  const functions = fs.readdirSync(functionsDir).filter(file => file.endsWith('.js'));
  functions.forEach(func => {
    console.log(`‚úÖ ${func}`);
  });
} else {
  console.log('‚ùå Directorio netlify/functions no encontrado');
}

// Verificar puertos disponibles
console.log('\nüåê PUERTOS:');
const ports = [3000, 8888];
ports.forEach(port => {
  try {
    require('child_process').execSync(`lsof -i :${port}`, { stdio: 'ignore' });
    console.log(`‚ö†Ô∏è  Puerto ${port} est√° en uso`);
  } catch {
    console.log(`‚úÖ Puerto ${port} disponible`);
  }
});

console.log('\nüéØ RESUMEN:');
console.log('El sistema est√° listo para desarrollo local.');
console.log('\nüìã PR√ìXIMOS PASOS:');
console.log('1. Ejecutar: npm install');
console.log('2. Ejecutar: npm run dev:full');
console.log('3. Abrir: http://localhost:3000');
console.log('4. Login: admin / admin');
